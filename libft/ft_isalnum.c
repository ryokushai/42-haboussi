int		ft_isalnum(int c)
{
	unsigned char	c2;

	c2 = (unsigned char)c;
	if ((c >= '0' && c <= '9') || (c >= 'a' && c <= 'z')
			|| (c >= 'A' && c <= 'Z'))
		return (1);
	else
		return (0);
}
