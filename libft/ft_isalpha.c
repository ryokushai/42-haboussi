#include "libft.h"

int		ft_isalpha(int c)
{
	unsigned char c2;

	c2 = (unsigned char)c;
	if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z'))
	{
		return (1);
	}
	else
		return (0);
}
